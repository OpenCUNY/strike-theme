<h1><span style="font-family: 'arial black', sans-serif; color: #ff0000; font-size: 18pt;"><span class="highlight" style="color: #ffff00;">Compensation</span><span style="color: #ffffff;"> isn't aid.</span></span></h1>
<span style="font-size: 14pt;">OpenCUNY is cared for and nurtured by three student coordinators and a community board. You support their work through the Doctoral Students' Council (DSC), with funds from your <span class="highlight">student activity fee.</span></span>

<span style="font-size: 14pt;">In June 2018, Graduate Center administrators re-classified compensation paid to students for DSC work as financial aid. This move places strict limits who is eligible to receive money and how much; demeans student labor; and was imposed without notice or explanation.</span>

<span style="font-size: 14pt;">In September 2018, the DSC<span style="color: #ffff00;"> <a style="color: #ffff00;" href="http://cunydsc.org/download/resolutions/2018-2019-Resolutions/20180921_Resolution-Opposing-the-Re-Classification-of-Student-Leadership-Stipends-as-Financial-Aid-or-Assistance.pdf">passed a resolution</a></span> against the administrative action and called for a sustained organized protest. During the protest, and until fair compensation returns, OpenCUNY will continue to be available, but services and events will be limited. Please allow extra time for inquiries.</span>
